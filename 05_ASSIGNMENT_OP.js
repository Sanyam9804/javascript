let a = 10;

console.log("Initial value of a:", a);

// Simple Assignment
a = 20;
console.log("After = :", a);

// Add and Assign
a += 5;   // a = a + 5
console.log("After += :", a);

// Subtract and Assign
a -= 3;   // a = a - 3
console.log("After -= :", a);

// Multiply and Assign
a *= 2;   // a = a * 2
console.log("After *= :", a);

// Divide and Assign
a /= 4;   // a = a / 4
console.log("After /= :", a);

// Modulus and Assign
a %= 3;   // a = a % 3
console.log("After %= :", a);

// Exponent and Assign
a **= 2;  // a = a ** 2
console.log("After **= :", a);